package DesignSystems.ObjectOrientedDesignCentric.Games.CardGames.SetGame;

public class Card {
    String shape;
    String shading;
    String color;
    int number;

    public Card(String shape, String shading, String color, int number){
        this.shape = shape;
        this.shading = shading;
        this.color = color;
        this.number = number;
    }
}
